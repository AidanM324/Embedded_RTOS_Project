static void temperatureTask(void * pvParameters) {
	EventBits_t PubTemp_Bits;
	dataStruct_t uartStruct;

	printf("\nStarting Temperature Task\r\n");
	BSP_TSENSOR_Init();		//Initialise temperature sensor
	while(1) {
		PubTemp_Bits = xEventGroupWaitBits(Publish_EventGroup, tempBit, pdTRUE, pdTRUE, portMAX_DELAY);

		printf("Event Bit set, Publishing Temp\r\n\n");
		float tempF = BSP_TSENSOR_ReadTemp();
		uint16_t tempI = tempF*10;
		uartStruct.ID = temperatureID;
		uartStruct.dataField = tempI;

		xQueueSend(dataQueue, &uartStruct, 0);

		vTaskDelay(pdMS_TO_TICKS(100));
	}
}

