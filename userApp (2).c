/*--------------------------------------
 * Initialisation Task
 * 1. Connects to ubidots MQTT broker
 * 2. Initialises temperature sensor
 * 3. Creates semaphores
 * 4. Creates temperature and RTC tasks
 * 5. Subscribes to LED_Control variable
 * 6. Task deletes itself
 ----------------------------------------*/
static void initTask(void * pvParameters) {
	uint32_t ret;
	printf("Starting Init Task\r\n");

	while(1) {
		HAL_GPIO_WritePin(LED2_GPIO_Port, LED2_Pin, GPIO_PIN_RESET);
		brokerConnect(&client);	//connect to WiFi access point and then to MQTT broker
		publishSemaphore = xSemaphoreCreateBinary();
		vQueueAddToRegistry(publishSemaphore, "Publish Semaphore");
		oneSecondSemaphore = xSemaphoreCreateBinary();
		vQueueAddToRegistry(oneSecondSemaphore, "RTC Semaphore");
		timerSemaphore = xSemaphoreCreateBinary();
		vQueueAddToRegistry(timerSemaphore, "Timer Semaphore");
		Publish_EventGroup = xEventGroupCreate();
		Button_EventGroup = xEventGroupCreate();
		xEventGroupSetBits(Button_EventGroup, buttonBit);
		dataQueue = xQueueCreate(5, sizeof(dataStruct_t));
		vQueueAddToRegistry(dataQueue, "Data Queue");

		autoReloadTimer = xTimerCreate("Auto Reload ", pdMS_TO_TICKS(4000), pdTRUE, 0, autoReloadTimerCallback);
		ledTimer = xTimerCreate("LED Reload ", pdMS_TO_TICKS(1000), pdTRUE, 0, ledTimerCallback);
		xTimerStart(autoReloadTimer, 0);

		__HAL_TIM_CLEAR_IT(&htim6, TIM_IT_UPDATE);
		HAL_NVIC_GetPendingIRQ(TIM6_DAC_IRQn);
		HAL_TIM_Base_Start_IT(&htim6);
		HAL_UART_Receive_IT(&huart1, &ch, 1);

		if(xTaskCreate(temperatureTask, "Temperature Task", 200, NULL, configMAX_PRIORITIES - 5, NULL) == pdTRUE) {
			printf("Temperature task created\n\r");
		}
		else {
			printf("Could not create temperature task\n\r");
		}
		if(xTaskCreate(humidityTask, "Humidity Task", 200, NULL, configMAX_PRIORITIES - 5, NULL) == pdTRUE) {
			printf("Humidity task created\n\r");
		}
		else {
			printf("Could not create humidity task\n\r");
		}
		if(xTaskCreate(RTC_Task, "RTC Task", 500, NULL, configMAX_PRIORITIES - 2, NULL) == pdTRUE) {
			printf("RTC task created\n\r");
		}
		else {
			printf("Could not create RTC task\n\r");
		}
		if(xTaskCreate(pressureTask, "Pressure Task", 200, NULL, configMAX_PRIORITIES - 4, NULL) == pdTRUE) {
			printf("Pressure task created\n\r");
		}
		else {
			printf("Could not create Pressure task\n\r");
		}
		if(xTaskCreate(accelerometerTask, "Accelerometer Task", 500, NULL, configMAX_PRIORITIES - 6, &accTaskHandle) == pdTRUE) {
			printf("Accelerometer task created\n\r");
		}
		else {
			printf("Could not create Accelerometer task\n\r");
		}
		if(xTaskCreate(displayTask, "Display Task", 500, NULL, configMAX_PRIORITIES - 3, NULL) == pdTRUE) {
			printf("Display task created\n\r");
		}
		else {
			printf("Could not create Display task\n\r");
		}


		//Subscribe to topics here
		//change the device name and variable name in the function call to match your Ubidots configuration
		ret = MQTTSubscribe(&client, "/v1.6/devices/rtosproject/ledonoff", QOS0, (subscribeMessageHandler));
		if (ret != MQSUCCESS) {
			printf("\n\rSubscribe failed: %ld\n\r", ret);
		}
		else {
			printf("\n\rSubscribed to topic \n\r");
			ret = MQTTYield(&client, 500);
		}


		//change the device name and variable name in the function call to match your Ubidots configuration
		ret = MQTTSubscribe(&client, "/v1.6/devices/rtosproject/ledslider", QOS0, (subscribeMessageHandler));
		if (ret != MQSUCCESS) {
			printf("\n\rSubscribe failed: %ld\n\r", ret);
		}
		else {
			printf("\n\rSubscribed to topic \n\r");
			ret = MQTTYield(&client, 500);
		}


		printf("Deleting Init Task\r\n\n");
		vTaskDelete(NULL);
	}
}

