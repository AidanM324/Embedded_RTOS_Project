/*--------------------------------------------------------------------
 * EXTI interrupt handler callback function
 * Will give a semaphore when the user button is pressed
 --------------------------------------------------------------------*/
void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin)
{
	BaseType_t xHigherPriorityTaskWoken;
	switch (GPIO_Pin)
	{
	case (GPIO_PIN_1):	//IRQ generated by WiFi module
		{
		SPI_WIFI_ISR();
		break;
		}

	case (BUTTON_EXTI13_Pin) :	//IRQ generated by user button
		{
			dataStruct_t uartStruct;
			uint8_t buttonState = 0;   // 0 = OFF, 1 = ON
			char button[25];
			static uint32_t lastTick = 0;
			uint32_t now = xTaskGetTickCountFromISR();

			// 20ms debounce (MANDATORY for EXTI13)
			if ((now - lastTick) < pdMS_TO_TICKS(20)){
			    break;
			}

			lastTick = now;


			EventBits_t bits = xEventGroupGetBitsFromISR(Button_EventGroup);
			if (bits & buttonBit){

			   xEventGroupClearBitsFromISR(Button_EventGroup, buttonBit);
			   HAL_TIM_Base_Stop_IT(&htim6);

			   buttonState = 1;
			   uartStruct.ID = buttonID;
			   uartStruct.dataField = buttonState;

			   xHigherPriorityTaskWoken = pdFALSE;
			   xQueueSendFromISR(dataQueue, &uartStruct, &xHigherPriorityTaskWoken);
			   portYIELD_FROM_ISR(xHigherPriorityTaskWoken);
			   //printf("DISPLAY PAUSED\r\n");
			   }
			else{
				// BLOCKED, RESUME display task
				buttonState = 0;
				uartStruct.ID = buttonID;
				uartStruct.dataField = buttonState;
				xHigherPriorityTaskWoken = pdFALSE;
				xQueueSendFromISR(dataQueue, &uartStruct, &xHigherPriorityTaskWoken);
				portYIELD_FROM_ISR(xHigherPriorityTaskWoken);

			    xEventGroupSetBitsFromISR(Button_EventGroup, buttonBit, &xHigherPriorityTaskWoken);
			    portYIELD_FROM_ISR(xHigherPriorityTaskWoken);

			    HAL_TIM_Base_Start_IT(&htim6);

			    //printf("DISPLAY RESUMED\r\n");
			   }

		}

	default:
	{
		break;
	}
	}
}

void HAL_UART_RxCpltCallback (UART_HandleTypeDef * huart){
	BaseType_t xHigherPriorityTaskWoken = pdFALSE;
	if(huart->Instance == USART1){
		switch(ch){
		case 't':

			xEventGroupSetBitsFromISR(Publish_EventGroup, tempBit, &xHigherPriorityTaskWoken);
			portYIELD_FROM_ISR(xHigherPriorityTaskWoken);
			printf("\nCharacter t received, sending temperature to queue\r\n");
			break;

		case 'h':

			xEventGroupSetBitsFromISR(Publish_EventGroup, humBit, &xHigherPriorityTaskWoken);
			portYIELD_FROM_ISR(xHigherPriorityTaskWoken);
			printf("\nCharacter h received, sending humidity to queue\r\n");
			break;

		default:
			break;
		}

		HAL_UART_Receive_IT(&huart1, &ch, 1);
	}
}


void TIM6_Handler() {
	BaseType_t xHigherPriorityTaskWoken = pdFALSE;
	xSemaphoreGiveFromISR(timerSemaphore, &xHigherPriorityTaskWoken);
	portYIELD_FROM_ISR(xHigherPriorityTaskWoken);
}

/*--------------------------------------------------------------------
 * RTC timer event interrupt handler callback function
 * Runs every second and gives a semaphore if the RTC task is running
 --------------------------------------------------------------------*/
void HAL_RTCEx_WakeUpTimerEventCallback (RTC_HandleTypeDef * hrtc) {
	BaseType_t xHigherPriorityTaskWoken = pdFALSE;
	if(RTC_TaskRunning){
		xHigherPriorityTaskWoken = pdFALSE;
		xSemaphoreGiveFromISR(oneSecondSemaphore, &xHigherPriorityTaskWoken);
		portYIELD_FROM_ISR(xHigherPriorityTaskWoken);
	}
}

